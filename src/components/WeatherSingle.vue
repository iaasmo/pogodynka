<script>
export default {
  props: {
    current: Object
  },
  methods: {
    unixToHumanDate( unixDate ){
      const dt = new Date( unixDate * 1000)
      return dt.toLocaleDateString()
    },
    simplifyTemp( temp ){
      if(typeof temp != "object") return temp;
      return temp.min+" - "+temp.max;
    }
  }
}
</script>
